package 有效的括号;

import java.util.List;

public class a {
    public static void main(String[] args) {
        List<Integer> integers = List.of(9, 8, 7, 6, 5, 4, 3, 2, 1, 0);
        integers.forEach(x -> {
            System.out.print(x);
            System.out.print(" ");
        });
        System.out.println("Hello, World!");
    }
}
